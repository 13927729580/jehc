var xtCityWinEdit;
var xtCityFormEdit;
function updateXtCity(){
	var record = grid.getSelectionModel().selected;
	if(record.length == 0){
		msgTishi('请选择要修改的一项！');
		return;
	}
	initXtCityFormEdit();
	xtCityWinEdit = Ext.create('Ext.Window',{
		layout:'fit',
		width:800,
		height:400,
		maximizable:true,
		minimizable:true,
		animateTarget:document.body,
		plain:true,
		modal:true,
		title:'编辑信息',
		listeners:{
			minimize:function(win,opts){
				if(!win.collapse()){
					win.collapse();
				}else{
					win.expand();
				}
			}
		},
		items:xtCityFormEdit,
		buttons:[{
			text:'保存',
			itemId:'save',
			handler:function(button){
				submitForm(xtCityFormEdit,'../xtCityController/updateXtCity',grid,xtCityWinEdit,false,true);
			}
		},{
			text:'关闭',
			itemId:'close',
			handler:function(button){
				button.up('window').close();
			}
		}]
	});
	xtCityWinEdit.show();
	loadFormData(xtCityFormEdit,'../xtCityController/getXtCityById?xt_cityID='+ record.items[0].data.xt_cityID);
}
